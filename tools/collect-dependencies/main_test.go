func TestImportPathsFromManifestIncludesNativeRoots(t *testing.T) {
	tmp := t.TempDir()
	repoRoot = tmp

	writeTestFile(t, filepath.Join(tmp, "imports.yaml"), `imports:
  - path: src/libraries/go/lib
    authoritative_source: native
  - path: src/services/demo
    repo: https://example.com/demo.git
    commit: deadbeef
`)

	got, err := importPathsFromManifest(filepath.Join(tmp, "imports.yaml"))
	if err != nil {
		t.Fatalf("importPathsFromManifest failed: %v", err)
	}

	want := []string{
		filepath.Join(tmp, "src/libraries/go/lib"),
		filepath.Join(tmp, "src/services/demo"),
	}
	if len(got) != len(want) {
		t.Fatalf("importPathsFromManifest returned %d paths, want %d (%v)", len(got), len(want), got)
	}
	for i := range want {
		if got[i] != want[i] {
			t.Fatalf("importPathsFromManifest path[%d] = %q, want %q", i, got[i], want[i])
		}
	}
}

func writeTestFile(t *testing.T, path, content string) {
	t.Helper()
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatalf("mkdir %s: %v", path, err)
	}
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatalf("write %s: %v", path, err)
	}
}

func assertFileContains(t *testing.T, path, needle string) {
	t.Helper()
	raw, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read %s: %v", path, err)
	}
	if !strings.Contains(string(raw), needle) {
		t.Fatalf("%s does not contain %q\n%s", path, needle, string(raw))
	}
}

func assertFileNotContains(t *testing.T, path, needle string) {
	t.Helper()
	raw, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read %s: %v", path, err)
	}
	if strings.Contains(string(raw), needle) {
		t.Fatalf("%s unexpectedly contains %q\n%s", path, needle, string(raw))
	}
}

func assertFileMissing(t *testing.T, path string) {
	t.Helper()
	if _, err := os.Stat(path); !os.IsNotExist(err) {
		t.Fatalf("expected %s to be missing, got err=%v", path, err)
	}
}

func assertFileEquals(t *testing.T, path, want string) {
	t.Helper()
	raw, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read %s: %v", path, err)
	}
	if string(raw) != want {
		t.Fatalf("%s content mismatch\nwant:\n%s\ngot:\n%s", path, want, string(raw))
	}
}

