func main() {
	if err := run(os.Args[1:], os.Stdout); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func run(argv []string, stdout *os.File) error {
	parsed, err := parseArgs(argv)
	if err != nil {
		return err
	}

	cfg, err := loadConfig(parsed.ConfigPath)
	if err != nil {
		return err
	}

	if parsed.GoWorkOutputPath != "" {
		renderedGoWork, err := renderGoWork(cfg, parsed.ConfigPath)
		if err != nil {
			return err
		}
		if err := os.WriteFile(parsed.GoWorkOutputPath, []byte(renderedGoWork), 0o644); err != nil {
			return err
		}
	}

	if parsed.ReleaseOutputPath != "" {
		renderedRelease, err := renderReleasePipeline(cfg, parsed.ConfigPath)
		if err != nil {
			return err
		}
		if err := os.WriteFile(parsed.ReleaseOutputPath, []byte(renderedRelease), 0o644); err != nil {
			return err
		}
	}

	if parsed.OutputPath == "" {
		// Only a non-default output path was requested (go-work
		// and/or release-output); skip the default child-pipeline
		// stdout dump.
		if parsed.GoWorkOutputPath != "" || parsed.ReleaseOutputPath != "" {
			return nil
		}
	}

	rendered, err := renderPipeline(cfg, parsed.ConfigPath)
	if err != nil {
		return err
	}

	if parsed.OutputPath == "" {
		_, err = stdout.WriteString(rendered)
		return err
	}

	return os.WriteFile(parsed.OutputPath, []byte(rendered), 0o644)
}

func parseArgs(argv []string) (args, error) {
	fs := flag.NewFlagSet("generate-subproject-ci", flag.ContinueOnError)
	fs.SetOutput(os.Stderr)

	parsed := args{
		ConfigPath: "tools/ci/subproject-validations.yaml",
	}
	fs.StringVar(&parsed.ConfigPath, "config", parsed.ConfigPath, "path to subproject validation config")
	fs.StringVar(&parsed.OutputPath, "output", "", "path to write generated child pipeline yaml")
	fs.StringVar(&parsed.GoWorkOutputPath, "go-work-output", "", "path to write generated go.work")
	fs.StringVar(&parsed.ReleaseOutputPath, "release-output", "", "path to write generated release-pipeline yaml")

	if err := fs.Parse(argv); err != nil {
		return args{}, err
	}
	if fs.NArg() != 0 {
		return args{}, fmt.Errorf("unexpected arguments: %s", strings.Join(fs.Args(), " "))
	}
	return parsed, nil
}

func loadConfig(path string) (configFile, error) {
	raw, err := os.ReadFile(path)
	if err != nil {
		return configFile{}, err
	}

	var cfg configFile
	if err := yaml.Unmarshal(raw, &cfg); err != nil {
		return configFile{}, err
	}
	if err := validateConfig(cfg); err != nil {
		return configFile{}, err
	}
	return cfg, nil
}

func validateConfig(cfg configFile) error {
	if cfg.Version != 1 {
		return fmt.Errorf("unsupported config version %d", cfg.Version)
	}
	if len(cfg.DefaultTags) == 0 {
		return errors.New("default_tags must not be empty")
	}
	// `profiles` may be empty when every subproject is release-only
	// (no validation jobs). Per-subproject validation below catches
	// references to undeclared profile names.
	if cfg.GoWork != nil {
		if strings.TrimSpace(cfg.GoWork.Go) == "" {
			return errors.New("go_work.go must not be empty")
		}
	}

	seenSubprojects := map[string]struct{}{}
	for _, subproject := range cfg.Subprojects {
		if strings.TrimSpace(subproject.ID) == "" {
			return errors.New("subproject id must not be empty")
		}
		if _, exists := seenSubprojects[subproject.ID]; exists {
			return fmt.Errorf("duplicate subproject id %q", subproject.ID)
		}
		seenSubprojects[subproject.ID] = struct{}{}
		if strings.TrimSpace(subproject.Path) == "" {
			return fmt.Errorf("subproject %q path must not be empty", subproject.ID)
		}
		if subproject.GoWork && cfg.GoWork == nil {
			return fmt.Errorf("subproject %q sets go_work but config does not define top-level go_work settings", subproject.ID)
		}
		// A subproject must declare at least one of `profile` (for the
		// child-pipeline validation jobs) or `release` (for the
		// release-pipeline auto-version + image-push jobs). The two
		// are orthogonal: a service can validate via the profile and
		// also opt into auto-released images.
		hasProfile := strings.TrimSpace(subproject.Profile) != ""
		hasRelease := subproject.Release != nil
		if !hasProfile && !hasRelease {
			return fmt.Errorf("subproject %q must set profile or release (or both)", subproject.ID)
		}
		if hasProfile {
			profile, ok := cfg.Profiles[subproject.Profile]
			if !ok {
				return fmt.Errorf("subproject %q references unknown profile %q", subproject.ID, subproject.Profile)
			}
			if strings.TrimSpace(profile.Image) == "" {
				return fmt.Errorf("profile %q image must not be empty", subproject.Profile)
			}
			if len(profile.Checks) == 0 {
				return fmt.Errorf("profile %q must define at least one check", subproject.Profile)
			}
			for _, chk := range profile.Checks {
				if err := validateCheck(subproject.Profile, chk); err != nil {
					return err
				}
			}
		}
		if hasRelease {
			if err := validateRelease(subproject.ID, subproject.Release); err != nil {
				return err
			}
		}
	}
	return nil
}

func validateRelease(id string, rel *releaseConfig) error {
	if strings.TrimSpace(rel.ServiceName) == "" {
		return fmt.Errorf("subproject %q release.service_name must not be empty", id)
	}
	if len(rel.ImagePushTargets) == 0 {
		return fmt.Errorf("subproject %q release.image_push_targets must not be empty", id)
	}
	seenTargets := map[string]struct{}{}
	for _, t := range rel.ImagePushTargets {
		if strings.TrimSpace(t.Name) == "" {
			return fmt.Errorf("subproject %q has release.image_push_targets entry with empty name", id)
		}
		if _, dup := seenTargets[t.Name]; dup {
			return fmt.Errorf("subproject %q has duplicate release.image_push_targets entry %q", id, t.Name)
		}
		seenTargets[t.Name] = struct{}{}
		if strings.TrimSpace(t.BazelTarget) == "" {
			return fmt.Errorf("subproject %q release target %q bazel_target must not be empty", id, t.Name)
		}
		switch t.Auth.Type {
		case "vault":
			if strings.TrimSpace(t.Auth.VaultKey) == "" {
				return fmt.Errorf("subproject %q release target %q vault auth requires vault_key", id, t.Name)
			}
		case "ci_var":
			if strings.TrimSpace(t.Auth.CIVar) == "" {
				return fmt.Errorf("subproject %q release target %q ci_var auth requires ci_var", id, t.Name)
			}
		default:
			return fmt.Errorf("subproject %q release target %q has unsupported auth type %q (want \"vault\" or \"ci_var\")", id, t.Name, t.Auth.Type)
		}
	}
	if rel.Helm != nil {
		if strings.TrimSpace(rel.Helm.ChartPath) == "" {
			return fmt.Errorf("subproject %q release.helm.chart_path must not be empty", id)
		}
		if len(rel.Helm.PushTargets) == 0 {
			return fmt.Errorf("subproject %q release.helm.push_targets must not be empty", id)
		}
		seenHelm := map[string]struct{}{}
		for _, t := range rel.Helm.PushTargets {
			if strings.TrimSpace(t.Name) == "" {
				return fmt.Errorf("subproject %q has release.helm.push_targets entry with empty name", id)
			}
			if _, dup := seenHelm[t.Name]; dup {
				return fmt.Errorf("subproject %q has duplicate release.helm.push_targets entry %q", id, t.Name)
			}
			seenHelm[t.Name] = struct{}{}
			hasLiteral := strings.TrimSpace(t.NGCPath) != ""
			hasVar := strings.TrimSpace(t.NGCPathVar) != ""
			if hasLiteral == hasVar {
				return fmt.Errorf("subproject %q helm push_target %q must set exactly one of ngc_path or ngc_path_var", id, t.Name)
			}
			if strings.TrimSpace(t.NGCKeyVar) == "" {
				return fmt.Errorf("subproject %q helm push_target %q ngc_key_var must not be empty", id, t.Name)
			}
		}
	}
	return nil
}

func validateCheck(profileName string, chk check) error {
	if strings.TrimSpace(chk.ID) == "" {
		return fmt.Errorf("profile %q has check with empty id", profileName)
	}
	switch chk.Type {
	case "go-vendor":
		return nil
	case "go-codegen":
		if strings.TrimSpace(chk.Command) == "" {
			return fmt.Errorf("profile %q check %q must set command", profileName, chk.ID)
		}
		return nil
	case "go-unit-tests":
		return nil
	case "shell":
		if strings.TrimSpace(chk.Command) == "" {
			return fmt.Errorf("profile %q check %q must set command", profileName, chk.ID)
		}
		return nil
	case "go-workspace-shell":
		if strings.TrimSpace(chk.Command) == "" {
			return fmt.Errorf("profile %q check %q must set command", profileName, chk.ID)
		}
		return nil
	default:
		return fmt.Errorf("profile %q check %q uses unsupported type %q", profileName, chk.ID, chk.Type)
	}
}

func renderPipeline(cfg configFile, configPath string) (string, error) {
	pipeline := generatedPipeline{
		ConfigPath:  configPath,
		DefaultTags: cfg.DefaultTags,
	}

	for _, subproject := range cfg.Subprojects {
		// Subprojects can declare only `release:` (no profile) when
		// they ship via the umbrella's per-service Bazel job plus the
		// generator-emitted release pipeline. They contribute nothing
		// to the validation child pipeline.
		if strings.TrimSpace(subproject.Profile) == "" {
			continue
		}
		profile := cfg.Profiles[subproject.Profile]
		for _, chk := range profile.Checks {
			job, err := buildJob(cfg.SharedChangePaths, profile, subproject, chk)
			if err != nil {
				return "", err
			}
			pipeline.Jobs = append(pipeline.Jobs, job)
		}
	}

	// GitLab fails a triggered child pipeline that ends up with zero jobs (for
	// example, when a change only touches files not listed in any job's
	// `changes:` rule, like a docs-only MR). Emit an unconditional sentinel so
	// the child pipeline always has at least one job to run.
	pipeline.Jobs = append(pipeline.Jobs, generatedJob{
		Name:      "subproject-validations-sentinel",
		Stage:     "validate",
		Image:     "golang:1.26-bookworm",
		AlwaysRun: true,
		Script:    []string{"echo 'subproject-validations: no path-gated jobs matched this change set.'"},
	})

	tmpl, err := template.New("pipeline").Funcs(template.FuncMap{
		"yamlValue": yamlScalar,
	}).Parse(pipelineTemplate)
	if err != nil {
		return "", err
	}

	view := struct {
		ConfigPath  string
		DefaultTags []string
		Jobs        []generatedJobView
	}{
		ConfigPath:  pipeline.ConfigPath,
		DefaultTags: pipeline.DefaultTags,
		Jobs:        make([]generatedJobView, 0, len(pipeline.Jobs)),
	}
	for _, job := range pipeline.Jobs {
		view.Jobs = append(view.Jobs, generatedJobView{
			Name:          job.Name,
			Stage:         job.Stage,
			Image:         job.Image,
			Variables:     job.Variables,
			VariablePairs: sortMap(job.Variables),
			Changes:       job.Changes,
			AlwaysRun:     job.AlwaysRun,
			Script:        job.Script,
			Coverage:      job.Coverage,
			Artifacts:     job.Artifacts,
		})
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, view); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// renderReleasePipeline returns the YAML body of the release-jobs
// file (typically tools/ci/generated-release-jobs.yml). The umbrella
// .gitlab-ci.yml `include: local: ...`s this file. One block per
// subproject that declared `release:` in subproject-validations.yaml.
func renderReleasePipeline(cfg configFile, configPath string) (string, error) {
	view := releasePipelineView{ConfigPath: configPath}

	for _, sp := range cfg.Subprojects {
		if sp.Release == nil {
			continue
		}

		// YAML anchor names allow a-zA-Z0-9-_ but not all chars; use
		// a sanitized form to stay portable.
		anchor := strings.ReplaceAll(sp.ID, "-", "_") + "_release_paths"

		svc := releaseServiceView{
			ID:                  sp.ID,
			ServiceName:         sp.Release.ServiceName,
			Path:                sp.Path,
			ChangePaths:         sp.ChangePaths,
			RulesAnchor:         anchor,
			SlackChannel:        sp.Release.SlackChannel,
			SonarqubeProjectKey: sp.Release.SonarqubeProjectKey,
		}

		for _, t := range sp.Release.ImagePushTargets {
			svc.Targets = append(svc.Targets, releaseTargetView{
				Name:           t.Name,
				BazelTarget:    t.BazelTarget,
				AuthType:       t.Auth.Type,
				CIVar:          t.Auth.CIVar,
				DockerAuthPath: t.DockerAuthPath,
			})
			if t.Auth.Type == "vault" && !svc.HasVaultTarget {
				svc.HasVaultTarget = true
				svc.VaultKey = t.Auth.VaultKey
			}
		}

		// Dedupe ci_var names across targets so the template emits one
		// "is var set" guard per unique variable. Order preserved by
		// first-seen across image_push_targets.
		seenCIVar := map[string]struct{}{}
		for _, t := range sp.Release.ImagePushTargets {
			if t.Auth.Type != "ci_var" {
				continue
			}
			if _, ok := seenCIVar[t.Auth.CIVar]; ok {
				continue
			}
			seenCIVar[t.Auth.CIVar] = struct{}{}
			svc.CIVars = append(svc.CIVars, t.Auth.CIVar)
		}

		if sp.Release.Helm != nil {
			helmView := &releaseHelmView{
				ChartPath: path.Join(sp.Path, sp.Release.Helm.ChartPath),
				Lint:      sp.Release.Helm.Lint,
			}
			for _, t := range sp.Release.Helm.PushTargets {
				// One of NGCPath / NGCPathVar is set (enforced by
				// validateRelease). NGCPathVar takes a `$VARNAME` shape
				// so the cds-component expands it via its `expand_vars`
				// input filter at pipeline runtime.
				ngcPath := t.NGCPath
				if ngcPath == "" {
					ngcPath = "$" + t.NGCPathVar
				}
				helmView.PushTargets = append(helmView.PushTargets, releaseHelmTargetView{
					Name:      t.Name,
					NGCPath:   ngcPath,
					NGCKeyVar: t.NGCKeyVar,
				})
			}
			svc.Helm = helmView
		}

		view.Services = append(view.Services, svc)
	}

	tmpl, err := template.New("release-pipeline").Funcs(template.FuncMap{
		"yamlValue": yamlScalar,
		"upper":     strings.ToUpper,
	}).Parse(releasePipelineTemplate)
	if err != nil {
		return "", err
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, view); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func renderGoWork(cfg configFile, configPath string) (string, error) {
	if cfg.GoWork == nil {
		return "", errors.New("go_work is not configured")
	}

	useSet := map[string]struct{}{}
	for _, path := range cfg.GoWork.Use {
		trimmed := filepath.ToSlash(strings.TrimSpace(path))
		trimmed = strings.TrimPrefix(trimmed, "./")
		if trimmed == "" {
			continue
		}
		useSet[trimmed] = struct{}{}
	}
	for _, subproject := range cfg.Subprojects {
		if !subproject.GoWork {
			continue
		}
		trimmed := filepath.ToSlash(strings.TrimSpace(subproject.Path))
		trimmed = strings.TrimPrefix(trimmed, "./")
		if trimmed == "" {
			continue
		}
		useSet[trimmed] = struct{}{}
	}

	if len(useSet) == 0 {
		return "", errors.New("go_work has no modules to include")
	}

	usePaths := make([]string, 0, len(useSet))
	for path := range useSet {
		usePaths = append(usePaths, path)
	}
	sort.Strings(usePaths)

	view := struct {
		ConfigPath string
		GoVersion  string
		Use        []string
	}{
		ConfigPath: configPath,
		GoVersion:  cfg.GoWork.Go,
		Use:        usePaths,
	}

	tmpl, err := template.New("goWork").Parse(goWorkTemplate)
	if err != nil {
		return "", err
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, view); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func buildJob(sharedChangePaths []string, profile profile, subproject subproject, chk check) (generatedJob, error) {
	data := subprojectTemplateData{
		ID:   subproject.ID,
		Path: subproject.Path,
	}

	jobName := sanitizeJobName(subproject.ID + "-" + chk.ID)
	stage := profile.Stage
	if strings.TrimSpace(stage) == "" {
		stage = "validate"
	}

	script, err := buildScript(profile, data, chk)
	if err != nil {
		return generatedJob{}, err
	}

	changes := append([]string{}, sharedChangePaths...)
	if len(subproject.ChangePaths) > 0 {
		changes = append(changes, subproject.ChangePaths...)
	} else {
		changes = append(changes, filepath.ToSlash(filepath.Join(subproject.Path, "**/*")))
	}

	image := profile.Image
	if strings.TrimSpace(chk.Image) != "" {
		image = chk.Image
	}

	job := generatedJob{
		Name:      jobName,
		Stage:     stage,
		Image:     image,
		Variables: profile.Variables,
		Changes:   changes,
		Script:    script,
	}

	if strings.TrimSpace(chk.Coverage) != "" {
		job.Coverage, err = renderTemplateString(chk.Coverage, data)
		if err != nil {
			return generatedJob{}, fmt.Errorf("job %q coverage: %w", jobName, err)
		}
	}
	if len(chk.Artifacts) > 0 {
		paths := make([]string, 0, len(chk.Artifacts))
		for _, artifactPath := range chk.Artifacts {
			rendered, err := renderTemplateString(artifactPath, data)
			if err != nil {
				return generatedJob{}, fmt.Errorf("job %q artifact path: %w", jobName, err)
			}
			paths = append(paths, rendered)
		}
		expireIn := chk.ExpireIn
		if strings.TrimSpace(expireIn) == "" {
			expireIn = "30 days"
		}
		job.Artifacts = &artifacts{
			Paths:    paths,
			ExpireIn: expireIn,
		}
	}

	return job, nil
}

func buildScript(profile profile, subproject subprojectTemplateData, chk check) ([]string, error) {
	prefix := workspaceSetupScript(profile, chk)

	switch chk.Type {
	case "go-vendor":
		return append(prefix,
			fmt.Sprintf("./tools/ci/check-go-vendor %s", shellQuote(subproject.Path)),
		), nil
	case "go-codegen":
		command, err := renderTemplateString(chk.Command, subproject)
		if err != nil {
			return nil, err
		}
		line := fmt.Sprintf("./tools/ci/check-go-codegen %s --command %s", shellQuote(subproject.Path), shellQuote(command))
		for _, install := range chk.Install {
			expanded, err := renderTemplateString(install, subproject)
			if err != nil {
				return nil, err
			}
			line += " --install " + shellQuote(expanded)
		}
		return append(prefix, line), nil
	case "go-unit-tests":
		resultsDir := chk.ResultsDir
		if strings.TrimSpace(resultsDir) == "" {
			resultsDir = filepath.ToSlash(filepath.Join("public", subproject.ID))
		}
		expandedResults, err := renderTemplateString(resultsDir, subproject)
		if err != nil {
			return nil, err
		}
		return append(prefix,
			fmt.Sprintf("./tools/ci/run-go-unit-tests %s --results-dir %s", shellQuote(subproject.Path), shellQuote(expandedResults)),
		), nil
	case "shell":
		command, err := renderTemplateString(chk.Command, subproject)
		if err != nil {
			return nil, err
		}
		return append(prefix,
			fmt.Sprintf("cd \"$CI_PROJECT_DIR/%s\" && %s", subproject.Path, command),
		), nil
	case "go-workspace-shell":
		command, err := renderTemplateString(chk.Command, subproject)
		if err != nil {
			return nil, err
		}
		return append(prefix,
			fmt.Sprintf("cd \"$CI_PROJECT_DIR/%s\" && GOWORK=\"$CI_PROJECT_DIR/go.work\" %s", subproject.Path, command),
		), nil
	default:
		return nil, fmt.Errorf("unsupported check type %q", chk.Type)
	}
}

func workspaceSetupScript(profile profile, chk check) []string {
	if chk.SkipWorkspaceSetup {
		return nil
	}

	if chk.Type == "go-workspace-shell" {
		return []string{"./tools/scripts/update-go-work"}
	}

	gowork, ok := profile.Variables["GOWORK"]
	if !ok {
		return nil
	}

	trimmed := strings.TrimSpace(gowork)
	if trimmed == "" || trimmed == "off" {
		return nil
	}

	return []string{"./tools/scripts/update-go-work"}
}

func renderTemplateString(input string, data subprojectTemplateData) (string, error) {
	tmpl, err := template.New("value").Option("missingkey=error").Parse(input)
	if err != nil {
		return "", err
	}
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func sanitizeJobName(value string) string {
	lower := strings.ToLower(value)
	re := regexp.MustCompile(`[^a-z0-9]+`)
	lower = re.ReplaceAllString(lower, "-")
	lower = strings.Trim(lower, "-")
	if lower == "" {
		return "subproject-validation"
	}
	return lower
}

func shellQuote(value string) string {
	if value == "" {
		return "''"
	}
	return "'" + strings.ReplaceAll(value, "'", `'"'"'`) + "'"
}

func yamlScalar(value any) string {
	raw, err := yaml.Marshal(value)
	if err != nil {
		panic(err)
	}
	return strings.TrimSpace(string(raw))
}

func sortMap(values map[string]string) []keyValue {
	if len(values) == 0 {
		return nil
	}
	keys := make([]string, 0, len(values))
	for key := range values {
		keys = append(keys, key)
	}
	sort.Strings(keys)

	out := make([]keyValue, 0, len(keys))
	for _, key := range keys {
		out = append(out, keyValue{Key: key, Value: values[key]})
	}
	return out
}
