// releaseConfig is the opt-in declaration for an umbrella-native
// service that wants auto-versioned releases via semantic-release.
// The generator emits compute-next-release-version-<id>,
// semantic-release-<id>, and <id>-image-push jobs into the release
// pipeline YAML the umbrella includes via `include: local: ...`.
//
// Tag format is hard-coded to ${service_name}-v${version} (same shape
// used by semantic-release-monorepo) so per-service tags don't
// collide with other services or with the umbrella's own tags.
type releaseConfig struct {
	ServiceName      string                  `yaml:"service_name"`
	ImagePushTargets []releaseImagePushTarget `yaml:"image_push_targets"`
	// SlackChannel, if non-empty, emits an additional
	// `<svc>-slack-notify` job that posts a release notification to
	// the named Slack channel after `semantic-release-<svc>` creates
	// the tag. Channel ID format is the Slack archives ID, e.g.
	// `C08S6KLCEJH` for `#nv-nvcf-cicd`. Empty disables the job.
	SlackChannel string `yaml:"slack_channel,omitempty"`
	// SonarqubeProjectKey, if non-empty, emits an additional
	// `<svc>-sonarqube-analysis` job that runs sonar-scanner against
	// the service's subtree under the named SonarQube project key.
	// Preserves the per-service Sonar dashboard each native cutover
	// had pre-archive (legacy SONARQUBE_PROJECT_KEY env var). Empty
	// disables the job; the umbrella's whole-tree sonarqube-analysis
	// continues to scan everything regardless.
	SonarqubeProjectKey string `yaml:"sonarqube_project_key,omitempty"`
	// Helm, if non-nil, declares that the service ships a Helm chart
	// the release pipeline should package and publish to NGC. The
	// generator emits `include:` directives at the top of the
	// generated YAML wiring in cds/cds-components/helm-package and
	// helm-push-ngc, parameterized per service. Chart version stamps
	// from semantic-release's NEXT_VERSION (same value as the OCI
	// image tag) so the chart, appVersion, and image tag stay
	// version-coherent. See HelmConfig for field-level docs.
	Helm *helmConfig `yaml:"helm,omitempty"`
}

// helmConfig declares the Helm chart packaging + push pipeline for a
// service. The generator wraps the cds-components helm-package@0.16.6
// + helm-push-ngc@0.16.6 templates, passing chart-version=$NEXT_VERSION
// at component-instantiation time so the packaged chart's version
// (and appVersion) line up with the service's semantic-release tag.
type helmConfig struct {
	// ChartPath is the directory containing Chart.yaml, relative to
	// the service's subtree root. For dns-cache that's `deploy`; for
	// nats-auth-callout it would be `chart`, etc.
	ChartPath string `yaml:"chart_path"`
	// Lint, if true, runs `helm lint` on the chart as part of the
	// package job. Off by default; turn on for new services where the
	// chart is under active development.
	Lint bool `yaml:"lint,omitempty"`
	// PushTargets is the list of NGC orgs the packaged chart gets
	// pushed to. The umbrella convention is that every service
	// includes ncp-dev (shared NGC_DEVOPS_API_KEY) and may also
	// include legacy targets (nv-ngc-devops, nvcf-internal) that
	// pre-Bazel CI used. At least one target is required.
	PushTargets []helmPushTarget `yaml:"push_targets"`
}

// helmPushTarget describes one NGC org the chart gets published to.
// Either NGCPath (hardcoded org/team path, e.g.
// "0651155215864979/ncp-dev") or NGCPathVar (the name of a CI
// variable that resolves to the path, e.g. "NGC_ORG_nv_ngc_devops")
// must be set; the generator validates exactly one is provided. The
// NGC key always comes from a CI variable name (NGCKeyVar) since
// the keys are protected secrets.
type helmPushTarget struct {
	// Name uniquely identifies this target within the service. Becomes
	// the suffix of the generated helm-push-<svc>-<name> job. Choose
	// short keys like "ncp-dev", "nv-ngc-devops", "nvcf-internal".
	Name string `yaml:"name"`
	// NGCPath is a literal org/team path on NGC, e.g.
	// "0651155215864979/ncp-dev". Mutually exclusive with NGCPathVar.
	NGCPath string `yaml:"ngc_path,omitempty"`
	// NGCPathVar is the name of a CI variable holding the NGC path
	// (e.g. "NGC_ORG_nv_ngc_devops"). The generator emits a `$VAR`
	// reference into the component input; resolution happens at
	// pipeline runtime. Mutually exclusive with NGCPath.
	NGCPathVar string `yaml:"ngc_path_var,omitempty"`
	// NGCKeyVar is the name of a CI variable holding the NGC API key
	// (e.g. "NGC_DEVOPS_API_KEY"). The generator emits a `$VAR`
	// reference into the component input. Required.
	NGCKeyVar string `yaml:"ngc_key_var"`
}

// releaseImagePushTarget describes one registry the image gets pushed
// to. Each target has its own Bazel push target (declared in the
// service's nvidia-internal/destinations.bzl) and its own auth source.
type releaseImagePushTarget struct {
	Name        string                 `yaml:"name"`
	BazelTarget string                 `yaml:"bazel_target"`
	Auth        releaseImagePushAuth   `yaml:"auth"`
	// DockerAuthPath, if set, is written as the key in
	// ~/.docker/config.json instead of the bare registry host. Use
	// when the service's base image lives on the same registry as the
	// push target (e.g. Rust services that pull `nvcr.io/nvidia/
	// distroless/cc` while pushing to `nvcr.io/<org>/<image>`).
	// rules_oci does longest-prefix matching on the auth keys; if the
	// scoped token's key is just "nvcr.io" it gets applied to every
	// nvcr.io pull including the public distroless base, and the
	// token's scoped permissions 403 the public read.
	//
	// Format: full path without scheme, e.g.
	//   `nvcr.io/ema5hzr4ziav/nvcf_autoscaling`
	//   `nvcr.io/0651155215864979/ncp-dev/nvcf-function-autoscaler`
	//
	// Optional. Empty falls back to the host-only key (works for
	// services whose base image isn't on the same registry).
	DockerAuthPath string `yaml:"docker_auth_path,omitempty"`
}

// releaseImagePushAuth holds the credential source for one push
// target. `type: vault` fetches NGC_REGISTRY + NGC_REGISTRY_TOKEN
